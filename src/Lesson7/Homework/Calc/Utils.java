package Lesson7.Homework.Calc;

public class Utils {

    public int Summa(int x, int y) {
        int sum = x + y;
        return sum;

    }

    public int Mult(int x, int y) {
        int mult = x * y;
        return mult;
    }
        public int Diff(int x, int y) {
            int diff = x - y;
            return diff;

    }
    public int Split(int x, int y) {
        int split = x / y;
        return split;

    }

}